let a = 3, b = 2;
// Operador para sumar +
let z = a+b;
console.log(`El resultado de la suma: ${z}`);

// Operador de resta -
z = a - b;
console.log(`El resultado de la resta: ${z}`);

// Operador de ultiplicacion *
z = a * b;
console.log(`El resultado de la multiplicacion: ${z}`);

// Operador de division
z = a / b;
console.log(`El resultado de la division: ${z}`);

// Operador de modulo
z = a % b;
console.log(`El resultado del modulo: ${z}`);

// Operador de exponente
z = a ** b;
console.log(`El resultado del exponente: ${z}`);

